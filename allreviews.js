    let maxReviewsToShow = initialMaxReviewsToShow;

    // Function to calculate the average rating and format it with two decimal places
    function calculateAverageRating(reviews) {
        if (reviews.length === 0) {
            return "0.0";
        }
        const totalRatings = reviews.reduce((sum, review) => sum + review.rating, 0);
        const average = totalRatings / reviews.length;
        return average.toFixed(2);
    }
  
    // Function to load product reviews based on the product name
    function loadProductReviews(productName) {
        currentProduct = productData[productName];

        if (!currentProduct) {
            // Handle the case where no product is found
            document.getElementById("average-rating-value").textContent = "0.0";
            document.getElementById("see-more-button").style.display = "none";
            document.getElementById("no-reviews-message").style.display = "block";
            return;
        }

        // Display the product name
        document.getElementById("product-name").textContent = productName;

        // Display the initial reviews
        displayReviews(currentProduct);
    }

    // Function to display reviews
    function displayReviews(product) {
        const averageRating = calculateAverageRating(product.reviews);
        const reviewList = document.getElementById("review-list");
        reviewList.innerHTML = '';

        const displayedReviews = product.reviews.slice(0, maxReviewsToShow);

        displayedReviews.forEach(review => {
            const reviewBox = document.createElement("div");
            reviewBox.classList.add("user-review-box");
            const imageHTML = review.image ? `<img src="${review.image}" alt="${review.name}" class="user-image">` : '';
            const userRatingStars = Array.from({ length: review.rating }, () => '<i class="fas fa-star" style="color: #e49e21;"></i>').join('');
            const userVerifiedIcon = '<i class="fas fa-check-circle" style="color:cornflowerblue; font-size:80%"></i>';
            reviewBox.innerHTML = `
                <p>${userVerifiedIcon} <strong>${review.name}</strong></p>
                <p>Rating: ${userRatingStars}</p>
                <p>${review.comment}</p>
                ${imageHTML}
            `;
            reviewList.appendChild(reviewBox);
        });

        // Display average rating with two decimal places
        document.getElementById("average-rating-value").textContent = averageRating;

        // Show or hide the "See More" button based on the number of reviews displayed
        const seeMoreButton = document.getElementById("see-more-button");
        seeMoreButton.style.display = maxReviewsToShow < product.reviews.length ? "block" : "none";

        // Hide the "No Reviews Yet" message
        document.getElementById("no-reviews-message").style.display = "none";
    }

